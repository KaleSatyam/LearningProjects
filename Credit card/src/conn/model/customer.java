package conn.model;

public class customer {
		String cust_name;
		String cust_mob;
		String cust_add;
		String cust_email;
		String cust_pass;
		int cust_ccno;
		public String getCust_name() {
			return cust_name;
		}
		public void setCust_name(String cust_name) {
			this.cust_name = cust_name;
		}
		public String getCust_mob() {
			return cust_mob;
		}
		public void setCust_mob(String cust_mob) {
			this.cust_mob = cust_mob;
		}
		public String getCust_add() {
			return cust_add;
		}
		public void setCust_add(String cust_add) {
			this.cust_add = cust_add;
		}
		public String getCust_email() {
			return cust_email;
		}
		public void setCust_email(String cust_email) {
			this.cust_email = cust_email;
		}
		public String getCust_pass() {
			return cust_pass;
		}
		public void setCust_pass(String cust_pass) {
			this.cust_pass = cust_pass;
		}
		public int getCust_ccno() {
			return cust_ccno;
		}
		public void setCust_ccno(int cust_ccno) {
			this.cust_ccno = cust_ccno;
		}
}
